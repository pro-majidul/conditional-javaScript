function returnd(num1, num2) {
    const total = num1 + num2;
    return total;
}
const point = returnd(40, 20);
console.log(point);




function totalPoint(x, y) {
    const add = x + y;
    const diff = x - y;
    const multi = add * diff;
    const div = multi / 2;
    return div
}
const number = totalPoint( 40 , 20);
console.log(number);



